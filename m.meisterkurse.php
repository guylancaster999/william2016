1948 in Aarau (Schweiz) geboren. Humanistisches Gymnasium Aarau. Erstes Musikstudium in Basel. Weitere Studien in Paris, London, Berlin, Baltimore und New York. 1981 Arnold-Schönberg-Preisträger für Klavier in Rotterdam. Danach international als Solist und Kammermusiker tätig. Repertoire-Schwerpunkt im frühen 20. Jahrhundert. Intensive Aufnahmetätigkeit mit über 300 Rundfunksendungen sowie 24 CDs, darunter Erstaufnahmen von Berg, Reger, Schreker, Webern und Zemlinsky. In jüngerer Zeit auch Hinwendung zu Hammerflügel und Clavichord. Als Komponist im wesentlichen Autodidakt. Gegen 80 Werke, vornehmlich für Tasteninstrumente, Kammermusik, Kammerorchester. Seit 1984 Klavierklasse an der Musik-Akademie Basel, Abteilung Hochschule für Musik. Sein erstes Buch &bdquo;Schönbergs Zeichen – Wege zur Interpretation seiner Klaviermusik&ldquo; erschien 2005 bei Lafite, Wien und auf spanisch bei Monte Ávila, Caracas. Die französische Fassung ist in Vorbereitung. Eine neue Buchpublikation &bdquo;Schumanns Zeichen&ldquo; ist für 2015 geplant. Publikationen in mehreren Fachzeitschriften und Zeitungen (dissonance, NZZ, SMZ u.a.). Organisator mehrerer grosser Symposien samt Begleitprogramm, zuletzt in Basel: 2010 Robert Schumann; 2011 Franz Liszt. 
